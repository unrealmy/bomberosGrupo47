    private void jbBuscarBrigadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarBrigadaActionPerformed
        // BuscarBrigada (para ver los Bomberos):
        
        //con el ActionPerformed
        
        try{
            int code=Integer.parseInt(jtcodBrig.getText());
            BomberoData buscar=new BomberoData();
            Bombero bu=new Bombero();
          
            ArrayList<Bombero> list = new ArrayList<>();
            list=buscar.listarBomberosXbrig(code);
            eliminarLimpiar();
        for (int i = 0; i < list.size(); i++) {
                 

                 
                 
                    modelo.addRow(new Object[]{list.get(i).getIdBombero(),list.get(i).getDni(),list.get(i).getNombreApellido(),list.get(i).getFechaNac(),list.get(i).getCelular(),list.get(i).getBrigada()});
                    
                 
             
             }
           
            
        }catch (NumberFormatException nf){
            JOptionPane.showMessageDialog(this, "Ingrese un Codigo Valido");
        }
        
    }//GEN-LAST:event_jbBuscarBrigadaActionPerformed


