    private void armarCabecera() {
        modelo.addColumn("idBombero");
        modelo.addColumn("dni");
        modelo.addColumn("NombreApellido");
        modelo.addColumn("FechaNac");
        modelo.addColumn("Celular");
        modelo.addColumn("CodBrigada");
        jtbomberos.setModel(modelo);
    }
public void eliminarLimpiar(){
        DefaultTableModel tb = (DefaultTableModel) jtbomberos.getModel();
        int a = jtbomberos.getRowCount()-1;
        for (int i = a; i >= 0; i--) {
        tb.removeRow(tb.getRowCount()-1);
        }
        
    }
